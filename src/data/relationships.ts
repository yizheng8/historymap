// ── 工具函数 ──

/** 构建 eventId → HistoryEvent 的查找映射 */
export const EVENT_MAP: Map<string, HistoryEvent> = new Map(
  HISTORY_EVENTS.map((e) => [e.id, e]),
);

/** 获取某事件的所有关系（作为 source 或 target） */
export function getRelationshipsForEvent(eventId: string): Relationship[] {
  return RELATIONSHIPS.filter(
    (r) => r.sourceId === eventId || r.targetId === eventId,
  );
}

/** 获取某事件的前因（本事件作为 target） */
export function getCauses(eventId: string): Relationship[] {
  return RELATIONSHIPS.filter((r) => r.targetId === eventId && r.type !== 'parallel');
}

/** 获取某事件的后果（本事件作为 source） */
export function getEffects(eventId: string): Relationship[] {
  return RELATIONSHIPS.filter((r) => r.sourceId === eventId && r.type !== 'parallel');
}

/** 获取某事件的并行事件 */
export function getParallels(eventId: string): Relationship[] {
  return RELATIONSHIPS.filter(
    (r) => r.type === 'parallel' && (r.sourceId === eventId || r.targetId === eventId),
  );
}
